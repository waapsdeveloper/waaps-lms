<script>
import Base from "./base.vue";
import { reactive, ref } from "vue";

export default {
  extends: Base,
  name: "profile",
  setup(){

    let profile = ref({
      terms: false,
      name: null,
      phone: null,
      cnic: null,
      address: null,
      qualification: null
    });
    return {profile};
  },

  mounted() {
    this.showTerms();
    this.getProfile();
  },




  methods: {
    async getProfile(){
        const res = await this.network().getUser();
        console.log(res);
        if(res[0]){
            let obj = res[0];
            this.profile.name = obj.name

            if(obj.profile){
                this.profile.cnic = obj.profile.nic
                this.profile.address = obj.profile.address
                this.profile.phone = obj.profile.phone
                this.profile.qualification = obj.profile.qualification
            }


        }
    },
    async showTerms(){

        // get the flag from API to see status
        const res = await this.network().getAcceptTerms()
        console.log(res)

        if(res && res.is_accepted){
            this.profile.terms = true;
        } else {
            $("#termsModal").modal('show');
        }

    },
    async acceptTerms(){
        console.log(this.profile.terms);

        // call API to send data
        let obj = {
            terms_accepted: this.profile.terms
        }

        const res = await this.network().acceptTerms(obj)
        console.log(res)
        $("#termsModal").modal('hide');
    },
    async saveProfileDetail(){
        console.log(this.profile);

        const res = await this.network().saveProfile(this.profile)
        console.log(res)
        this.getProfile();
        $("#profileModal").modal('hide')
    }
  }
};
</script>
